package math

import (
	"jvm/ch05/instructions/base"
	"jvm/ch05/rtda"
)

type ISHL struct {
	//int 左位移
	base.NoOperandsInstruction
}

func (self *ISHL) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := v1 << s
	stack.PushInt(result)
}

